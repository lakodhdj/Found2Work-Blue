FONT_NAME = "Times New Roman"
FONT_SIZE = Pt(11)


def set_run_font(run, bold=False):
    run.font.name = FONT_NAME
    run.font.size = FONT_SIZE
    run.bold = bold
    run._element.rPr.rFonts.set(qn("w:eastAsia"), FONT_NAME)


def add_paragraph(doc, text, align=WD_ALIGN_PARAGRAPH.JUSTIFY, bold=False, space_after=6, first_line_indent=None):
    p = doc.add_paragraph()
    p.alignment = align
    p.paragraph_format.space_after = Pt(space_after)
    p.paragraph_format.line_spacing = 1.15
    if first_line_indent is not None:
        p.paragraph_format.first_line_indent = Cm(first_line_indent)
    run = p.add_run(text)
    set_run_font(run, bold=bold)
    return p


def add_numbered_clause(doc, number, text, subitems=None):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
    p.paragraph_format.space_after = Pt(6)
    p.paragraph_format.line_spacing = 1.15
    p.paragraph_format.first_line_indent = Cm(0)
    run = p.add_run(f"{number}.\t{text}")
    set_run_font(run)
    if subitems:
        for item in subitems:
            sp = doc.add_paragraph()
            sp.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
            sp.paragraph_format.space_after = Pt(4)
            sp.paragraph_format.line_spacing = 1.15
            sp.paragraph_format.left_indent = Cm(1.25)
            sp.paragraph_format.first_line_indent = Cm(-0.63)
            sr = sp.add_run(item)
            set_run_font(sr)
    return p


