        "деловую репутацию Стороны 2.\n"
        "Описание размещённых сведений (текст отзыва/объявления, ссылка на страницу, дата размещения): "
        "______________________________________________________________________________________________\n"
        "______________________________________________________________________________________________.",

        "Сторона 1 обязуется в срок не позднее ___ (__________) календарных дней с даты подписания настоящего "
        "Соглашения:\n"
        "— удалить указанные в п. 2 сведения с интернет-платформы «Авито» и иных интернет-ресурсов (при наличии);\n"
        "— не размещать и не распространять в дальнейшем в сети «Интернет», в том числе в социальных сетях, "
        "мессенджерах и на интернет-площадках, сведения о Стороне 2, которые не соответствуют действительности "
        "и (или) порочат её честь, достоинство и деловую репутацию.",

        "Сторона 1 подтверждает, что обязательства по настоящему Соглашению принимает добровольно, осознаёт "
        "их правовые последствия и не действует под принуждением, угрозой, заблуждением или обманом.",

        "В случае нарушения Стороной 1 обязательств, предусмотренных п. 3 настоящего Соглашения, Сторона 2 "
