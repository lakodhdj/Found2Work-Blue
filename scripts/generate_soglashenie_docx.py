# -*- coding: utf-8 -*-
from docx import Document
from docx.shared import Pt, Cm
from docx.enum.text import WD_ALIGN_PARAGRAPH
from docx.oxml.ns import qn
import os

OUTPUT = os.path.join(os.path.dirname(os.path.dirname(__file__)), "Добровольное_соглашение_Ананидзе_Хасянова.docx")
FONT = "Times New Roman"
SIZE = Pt(11)


def font(run, bold=False):
    run.font.name = FONT
    run.font.size = SIZE
    run.bold = bold
    run._element.rPr.rFonts.set(qn("w:eastAsia"), FONT)


def p(doc, text, align=WD_ALIGN_PARAGRAPH.JUSTIFY, bold=False, after=6, indent=None, center=False):
    para = doc.add_paragraph()
    para.alignment = WD_ALIGN_PARAGRAPH.CENTER if center else align
    para.paragraph_format.space_after = Pt(after)
    para.paragraph_format.line_spacing = 1.15
    if indent:
        para.paragraph_format.first_line_indent = Cm(indent)
    r = para.add_run(text)
    font(r, bold)
    return para


def clause(doc, n, text):
    para = doc.add_paragraph()
    para.alignment = WD_ALIGN_PARAGRAPH.JUSTIFY
    para.paragraph_format.space_after = Pt(6)
    para.paragraph_format.line_spacing = 1.15
    r = para.add_run(f"{n}.\t{text}")
    font(r)


def main():
    doc = Document()
    s = doc.sections[0]
    s.top_margin = s.bottom_margin = Cm(2)
    s.left_margin = Cm(3)
    s.right_margin = Cm(1.5)
    doc.styles["Normal"].font.name = FONT
    doc.styles["Normal"].font.size = SIZE

    p(doc, "ДОБРОВОЛЬНОЕ СОГЛАШЕНИЕ", center=True, bold=True, after=6)
    p(doc, "об отказе от распространения сведений,\nпорочащих честь, достоинство и деловую репутацию", center=True, after=12)
